const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

const decode = (expr) =>
  expr
    .split("**********")
    .map((el) =>
      el
        .split(/(.{10})/)
        .filter((e) => !!e)
        .map((el) => el.split(/(.{2})/).filter((e) => !!e))
        .map((el) =>
          el.map((el) => {
            let num = Number(el);
            if (num < 10) return "";
            if (num === 10) return ".";
            else return "-";
          })
        )
        .map((el) => el.join(""))
        .reduce((acc, el) => acc.concat(MORSE_TABLE[el]), "")
    )
    .join(" ");

